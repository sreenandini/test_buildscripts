        private string CurrencySymbolofCulture(string CurrencyCulture)
        {
            return string.IsNullOrEmpty(CurrencyCulture) ? string.Empty : new RegionInfo(CurrencyCulture).CurrencySymbol;
        }

        private void LoadCommonParams(clsSPParams CommonParams)
        {
            CommonParams.CurrencyCulture = oReportBusiness.GetCurrentCurrenyCulture();
            CommonParams.CurrencySymbol = CurrencySymbolofCulture(CommonParams.CurrencyCulture);
            CommonParams.CurrentTime = DateTime.Now.ToString("HH:mm:ss");
            CommonParams.CurrentDate = DateTime.Now.ToString("dd-MMM-yyyy HH:mm:ss");
            DateTimeFormatInfo dfi = CultureInfo.CurrentCulture.DateTimeFormat;
            CommonParams.DateFormat = dfi.ShortDatePattern.ToString();
            CommonParams.ReportHeader = "";
            CommonParams.ProductVersion = oReportBusiness.GetBMCVersion();
            CommonParams.Language = oReportBusiness.GetCurrentCurrenyCulture();
            CommonParams.Denom = 0;
            CommonParams.runtype = 1;
            CommonParams.ExcludeZero = false;
            CommonParams.OnlyeFundEnabled = false;
        }

        private void AssignCommonParams(clsSPParams Source, clsSPParams Target)
        {
            Target.CurrencyCulture = Source.CurrencyCulture;
            Target.CurrencySymbol = Source.CurrencySymbol;
            Target.CurrentTime = Source.CurrentTime;
            Target.CurrentDate = Source.CurrentDate;
            Target.DateFormat = Source.DateFormat;
            Target.ReportHeader = Source.ReportHeader;
            Target.ProductVersion = Source.ProductVersion;
            Target.Language = Source.Language;
            Target.Denom = Source.Denom;
            Target.runtype = Source.runtype;
            Target.ExcludeZero = Source.ExcludeZero;
            Target.OnlyeFundEnabled = Source.OnlyeFundEnabled;
        }

